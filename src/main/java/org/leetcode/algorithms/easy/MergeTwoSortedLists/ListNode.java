package org.leetcode.algorithms.easy.MergeTwoSortedLists;

public class ListNode {
    public ListNode next;
    int val;

    public ListNode(int val) {
        this.val = val;
    }

    @Override
    public String toString() {
        return "ListNode{" +
                "val=" + val +
                ", next=" + next +
                '}';
    }

}
